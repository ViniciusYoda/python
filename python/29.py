tupla = () # atribui uma tupla vazia à variavel.
print(tupla)
tupla = (123,) # atribui uma tupla de apenas um item á variavel.
print(tupla)
tupla = (3.14, True, 29, 'oi1') # atribui uma nova tupla à variavel
print(tupla)
print(len(tupla)) # resulta na quantidade de itens da tupla;
print(tupla[1]) # acesso ao segundo item da tupla.
tupla[1] = False # tentativa de alteração da tupla.