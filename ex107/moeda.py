def aumentar(valor, percentual):
    """Aumenta o valor em uma porcentagem especificada."""
    return valor + (valor * percentual / 100)

def diminuir(valor, percentual):
    """Diminui o valor em uma porcentagem especificada."""
    return valor - (valor * percentual / 100)

def dobro(valor):
    """Retorna o dobro do valor."""
    return valor * 2

def metade(valor):
    """Retorna a metade do valor."""
    return valor / 2